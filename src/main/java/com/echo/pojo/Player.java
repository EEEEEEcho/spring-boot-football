package com.echo.pojo;

public class Player {
    private Integer playerId;
    private String playerName;
    private Integer attPenGoal;
    private Integer games;
    private Integer goals;
    private Integer minsPlayed;
    private Integer ontargetScoringAtt;
    private String playerlogo;
    private String team;
    private Integer teamId;
    private Integer totalScoringAtt;
    private Integer type;

    public Integer getPlayerId() {
        return playerId;
    }

    public void setPlayerId(Integer playerId) {
        this.playerId = playerId;
    }

    public String getPlayerName() {
        return playerName;
    }

    public void setPlayerName(String playerName) {
        this.playerName = playerName;
    }

    public Integer getAttPenGoal() {
        return attPenGoal;
    }

    public void setAttPenGoal(Integer attPenGoal) {
        this.attPenGoal = attPenGoal;
    }

    public Integer getGames() {
        return games;
    }

    public void setGames(Integer games) {
        this.games = games;
    }

    public Integer getGoals() {
        return goals;
    }

    public void setGoals(Integer goals) {
        this.goals = goals;
    }

    public Integer getMinsPlayed() {
        return minsPlayed;
    }

    public void setMinsPlayed(Integer minsPlayed) {
        this.minsPlayed = minsPlayed;
    }

    public Integer getOntargetScoringAtt() {
        return ontargetScoringAtt;
    }

    public void setOntargetScoringAtt(Integer ontargetScoringAtt) {
        this.ontargetScoringAtt = ontargetScoringAtt;
    }

    public String getPlayerlogo() {
        return playerlogo;
    }

    public void setPlayerlogo(String playerlogo) {
        this.playerlogo = playerlogo;
    }

    public String getTeam() {
        return team;
    }

    public void setTeam(String team) {
        this.team = team;
    }

    public Integer getTeamId() {
        return teamId;
    }

    public void setTeamId(Integer teamId) {
        this.teamId = teamId;
    }

    public Integer getTotalScoringAtt() {
        return totalScoringAtt;
    }

    public void setTotalScoringAtt(Integer totalScoringAtt) {
        this.totalScoringAtt = totalScoringAtt;
    }

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }
}
